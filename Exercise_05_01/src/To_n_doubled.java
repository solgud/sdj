import java.util.Scanner;

public class To_n_doubled
{

   public static void main(String[] args)
   {
      int i=0, n;
      Scanner key = new Scanner(System.in);

      // Get value of n
      System.out.println("Give me a number!");
      n = key.nextInt();
      
      while (i < n * 2)
      {
         System.out.print(i + ", ");
         i += 2;
      }
      System.out.print(n * 2);
      
      
   }

}
