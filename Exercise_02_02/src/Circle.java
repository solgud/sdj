
public class Circle
{

   public static void main(String[] args)
   {
      // Find the area of a circle
      
      // Instantiate variables
      double radius, area;
      
      // Set the radius
      radius = 22.5;
      
      // Use methods from the Math class to find area
      area = Math.PI * Math.pow(radius, 2);
      
      // Print result
      System.out.println(area);
   }

}
