
public class Sum
{

   public static void main(String[] args)
   {
      // Take three numbers and sum them
      
      // Declare and initialize variables
      int first = 7,
          second = 9,
          third = 13;
      
      // Combine values into a sum variable
      int sum = first + second + third;
      
      // Print the result
      System.out.print(sum);
   }

}
